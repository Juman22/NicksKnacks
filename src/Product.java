public class Product {
	public int id;
	public int sku;
	public String name;
	public String desc;
	public int price;
	public int quantity;
	public String img;
	
	public Product(int id, int sku, String name, String desc, int quantity, String img) {
		this.id = id;
		this.sku = sku;
		this.desc = desc;
		this.quantity = quantity;
		this.img = img;
	}
	
	
}
